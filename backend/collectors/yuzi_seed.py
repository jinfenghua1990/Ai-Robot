def seed_yuzi_dict():
    """upsert 预置席位（不重复插入，按 seat_name 唯一）"""
    import json as _json
    inserted, updated = 0, 0
    with get_db_session() as db:
        for row in SEED_SEATS:
            # 兼容 6/7 列两种格式（旧格式无 style）
            if len(row) == 7:
                seat_name, alias, group, style, region, tags, hot = row
            elif len(row) == 6:
                seat_name, alias, group, region, tags, hot = row
                style = '稳健'
            else:
                seat_name, alias, group, region, tags, hot = row[:6]
                style = '稳健'

            existing = db.query(YuziDict).filter(YuziDict.seat_name == seat_name).first()
            if existing:
                existing.yuzi_alias = alias
                existing.yuzi_group = group
                existing.style = style
                existing.region = region
                existing.tags = _json.dumps(tags, ensure_ascii=False)
                existing.hot_score = hot
                existing.is_active = True
                updated += 1
            else:
                db.add(YuziDict(
                    seat_name=seat_name,
                    yuzi_alias=alias,
                    yuzi_group=group,
                    style=style,
                    region=region,
                    tags=_json.dumps(tags, ensure_ascii=False),
                    is_active=True,
                    hot_score=hot,
                ))
                inserted += 1

        # 修复:对 DB 里所有同 alias 的席位,统一 style 为 SEED_SEATS 里该 alias 的 style
        # (处理用户之前手动加的"国泰海通/中信建投"等旧席位,它们 style 还是默认值)
        alias_style_map = {}
        for row in SEED_SEATS:
            if len(row) >= 7:
                alias_style_map[row[1]] = row[3]
        fixed = 0
        for alias, style in alias_style_map.items():
            n = db.query(YuziDict).filter(
                YuziDict.yuzi_alias == alias,
                YuziDict.style != style,
            ).update({YuziDict.style: style})
            if n:
                fixed += n
        db.commit()

    # 统计独立 alias 数
    with get_db_session() as db:
        alias_count = db.query(YuziDict.yuzi_alias).distinct().count()
        seat_count = db.query(YuziDict).count()
    print(f'[seed_yuzi_dict] inserted={inserted}, updated={updated}, '
          f'style_fixed={fixed}, total_seats={seat_count}, distinct_aliases={alias_count}')


if __name__ == '__main__':
    seed_yuzi_dict()
